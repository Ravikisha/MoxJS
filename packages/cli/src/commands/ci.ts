const previewSubCommand = new Command('preview')
  .description('Scaffold only the PR preview workflow (.github/workflows/pr-preview.yml)')
  .option('-d, --dir <path>', 'Workspace root directory', process.cwd())
  .option('--node <version>', 'Node.js version', '22')
  .option('--package-manager <pm>', 'Package manager', 'pnpm')
  .action(
    async (opts: { dir: string; node: string; packageManager: string }) => {
      const workspaceDir = path.resolve(opts.dir);
      const workflowsDir = path.join(workspaceDir, '.github', 'workflows');
      await fs.ensureDir(workflowsDir);

      const previewPath = path.join(workflowsDir, 'pr-preview.yml');
      await fs.outputFile(
        previewPath,
        buildPreviewWorkflow({ nodeVersion: opts.node, packageManager: opts.packageManager }),
        'utf8'
      );
      console.log(
        kleur.green('✓ PR preview workflow → ') +
          kleur.gray(path.relative(workspaceDir, previewPath))
      );
    }
  );

